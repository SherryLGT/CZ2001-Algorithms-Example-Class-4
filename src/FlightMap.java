	/**
	 * Get list of flights according to input size
	 */
	public int[][] getFlights(int size) {
		int[][] newFlights = new int[size][size];
		for(int i = 0; i < size; i++) {
			System.arraycopy(flights[i], 0, newFlights[i], 0, size);
		}
		return newFlights;
	}

